using UnityEngine;
using UnityEngine.SceneManagement;

public class PauseManager : MonoBehaviour
{
    public GameObject pauseMenuUI;
    private bool isPaused = false;

    void Update()
    {
        if (Input.GetKeyDown(KeyCode.Escape))  // O usa un botón en móvil si es tu caso
        {
            if (isPaused)
                Resume();
            else
                Pause();
        }
    }

    public void Resume()
    {
        pauseMenuUI.SetActive(false);
        Time.timeScale = 1f;
        isPaused = false;
    }

    public void Pause()
    {
        pauseMenuUI.SetActive(true);
        Time.timeScale = 0f;
        isPaused = true;
    }

    public void LoadMainMenu()
    {
        Time.timeScale = 1f;

        // ❗ IMPORTANTE: Cancelar los botones ganados en esta partida
        if (GameManager.Instance != null)
        {
            GameManager.Instance.CancelarBotonesDePartida();
        }

        SceneManager.LoadScene("MainMenu");
    }
}